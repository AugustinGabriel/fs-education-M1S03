public class VariavelConstante {

    public static void main(String[] args) {

        // Constante
        final int IDADE = 28;

        // Variável
        String nome = "Gabriel";

        System.out.println("O nome é: " + nome);
        System.out.println("A idade é: " + IDADE);

        nome = "Gabriel Augustin";

        System.out.println("O nome é: " + nome);
        System.out.println("A idade é: " + IDADE);

    }

}
